#include <unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	index;

	index = 0;
	while (src[index] != '\0' && index < n)
	{
		dest[index] = src[index];
		index++;
	}
	while (index < n)
	{
		dest[index] = '\0';
		index++;
	}
	return (dest);
}


#include <stdio.h>

int	main(void)
{	
	int n = 3;
	char des[10];
	char sr[] = "Panico!";
	printf("TESTE %s" ,ft_strncpy(des, sr, n));
}

