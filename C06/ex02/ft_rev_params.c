#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	len;
	int	a;

	len = argc;	
	a = 0;
	while (len > 1)
	{
		while (argv[len - 1][a] != '\0')
		{
			write(1, &argv[len - 1][a], 1);
			a++;
		}
		write(1, "\n", 1);
		a = 0; 
		len--;
	}
}
